    private void nuevaPila(){
        try {
            pila = new Pila(Integer.parseInt(txtTam.getText()));
            llenarTabla();
        } catch (NumberFormatException e) {
            txtTam.setText("0");
            txtTam.requestFocus();
        }
    }
    
    
    
