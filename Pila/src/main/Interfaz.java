    private void meter(){
        btnS.setEnabled(true);
        try {
            pila.meter(txtD.getText());
        } catch (IllegalStateException e) {
            showMessageDialog(this, e.getMessage());
            txtD.setText("");
            btnM.setEnabled(false);
        }
        llenarTabla();
        txtD.setText("");
    }
    
