    private void actualizarTbl(){
        dtm.setRowCount(0);
        int conta=0;
        Cancion temp=actual;
        
        if(!lista.estaVacia()){
            for (NodoDE i = lista.getInicioN(); i != lista.getFinN(); i=i.getSig()){
                dtm.setRowCount(dtm.getRowCount()+1);
                dtm.setValueAt(i.getCancion().getNombre(), conta, 0);
                conta++;
            }
            
        }
        
    }
    
    private void pausar(){
        reproduciendo=false;
        btnPlay.setIcon(new ImageIcon(Class.class.getResource("/Iconos/Play.png")));
        try {
            player.pause();
        } catch (BasicPlayerException ex) {
            Logger.getLogger(Interfaz.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    private void detener(){
        reproduciendo=false;
        btnPlay.setIcon(new ImageIcon(Class.class.getResource("/Iconos/Play.png")));
        try {
            player.stop();
        } catch (BasicPlayerException ex) {
            Logger.getLogger(Interfaz.class.getName()).log(Level.SEVERE, null, ex);
        }
        cont=0;
    }
    
    private void reproducir(){
        if(!lista.estaVacia()){
            reproduciendo = true;
            btnPlay.setIcon(new ImageIcon(Class.class.getResource("/Iconos/Pause.png")));
            if (cont <= 0) {
                try {
                    lblCancionActual.setText(actual.getNombre());
                    player.open(actual.getArchivo());
                    player.play();
                } catch (BasicPlayerException e) {
                    //e.printStackTrace();
                }
            } else {
                try {
                    player.resume();
                } catch (BasicPlayerException ex) {
                    //Logger.getLogger(Interfaz.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
            cont++;
        }
        
    }
    
    private void reproducir(Cancion c){
            reproduciendo = true;
            btnPlay.setIcon(new ImageIcon(Class.class.getResource("/Iconos/Pause.png")));
                try {
                    //player.open(actual.getArchivo());
                    player.open(c.getArchivo());
                    player.play();
                } catch (BasicPlayerException e) {
                    //e.printStackTrace();
                }
                
            cont++;
        
        
    }
    
